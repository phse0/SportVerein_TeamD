    private void initControls() throws RemoteException {
        
        List<ISportsmanDTO> assignedSportsman = assignController.loadPlayersOfTeam(tournamentTeam.getTournament(), tournamentTeam.getTeam());
        
        SportsManTableModel model = new SportsManTableModel(assignedSportsman);
        sportsmanTable.setModel(model);
        
        List<ISportsmanDTO> notAssignedSportsman = assignController.loadNotAssignedPlayersOfTeam(tournamentTeam.getTournament(), tournamentTeam.getTeam());
        for(ISportsmanDTO s : notAssignedSportsman) {
            cobSportsman.addItem(s);
        }
        
        btnSave.addActionListener(new AssignTournamentTeamPlayerListener(this, sportsmanTable, assignController));
        
    }
